var http = require("http");
var dispatcher = require("dispatcher");
var disp = new dispatcher();
var server = http.createServer(gestisciRichieste);

disp.aggiungiServizio("/getRicette", getRicette);
disp.aggiungiServizio("/getRicetteByFilter", getRicetteByFilter);
disp.aggiungiServizio("/addRicetta", addRicetta);


function gestisciRichieste(richiesta, risposta){
    console.log("Nuova richiesta arrivata");
    console.log(`${richiesta.method} - ${richiesta.url}`);

    disp.smista(richiesta, risposta);
}


function getRicette(richiesta, risposta){
    risposta.writeHead(200, {"Content-Type":"application/json"});
    risposta.write(JSON.stringify(ricette));
    risposta.end();
};

function getRicetteByFilter(richiesta, risposta)
{
    let parametri = new URL(richiesta.headers.host+richiesta.url).searchParams;
    let filtro = parametri.get("filtro").toLowerCase();
    
    let filter = ricette.filter(ricetta => 
        ricetta.titolo.toLowerCase().includes(filtro) || 
        ricetta.tipo.toLowerCase().includes(filtro)
    );
    
    risposta.writeHead(200, {"Content-Type": "application/json"});
    risposta.write(JSON.stringify(filter));
    risposta.end();
}

function addRicetta(richiesta, risposta)
{
    let body = "";
    richiesta.on("data", (infoParziali) => {
        body += infoParziali;
    })
    richiesta.on("end", () => {
        let aus = JSON.parse(body);
        ricette.push(aus);

        risposta.writeHead(200, {"Content-Type": "application/json"});
        risposta.write(JSON.stringify({message: "Ricetta aggiunta con successo!"}));
        risposta.end();
    });
}





